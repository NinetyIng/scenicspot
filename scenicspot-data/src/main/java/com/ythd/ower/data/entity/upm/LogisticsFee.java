package com.ythd.ower.data.entity.upm;

public class LogisticsFee {

	private String feeId;
	
	private String feeFirstNum;
	
	private String firstFee;
	
	private String followNum;
	
	private String followFee;
	
	private String areaNames;
	
	private String areaIds;
	
	private String defaultArea;
	

	public String getFeeId() {
		return feeId;
	}

	public void setFeeId(String feeId) {
		this.feeId = feeId;
	}

	public String getFeeFirstNum() {
		return feeFirstNum;
	}

	public void setFeeFirstNum(String feeFirstNum) {
		this.feeFirstNum = feeFirstNum;
	}

	public String getFirstFee() {
		return firstFee;
	}

	public void setFirstFee(String firstFee) {
		this.firstFee = firstFee;
	}

	public String getFollowNum() {
		return followNum;
	}

	public void setFollowNum(String followNum) {
		this.followNum = followNum;
	}

	public String getFollowFee() {
		return followFee;
	}

	public void setFollowFee(String followFee) {
		this.followFee = followFee;
	}

	public String getAreaNames() {
		return areaNames;
	}

	public void setAreaNames(String areaNames) {
		this.areaNames = areaNames;
	}

	public String getAreaIds() {
		return areaIds;
	}

	public void setAreaIds(String areaIds) {
		this.areaIds = areaIds;
	}

	public String getDefaultArea() {
		return defaultArea;
	}

	public void setDefaultArea(String defaultArea) {
		this.defaultArea = defaultArea;
	}
	
	
	

}
